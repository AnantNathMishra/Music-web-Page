var leftSongList = document.querySelector(".song-list");
var popularArt = document.querySelector(".popular-art");
var play = document.querySelector("#play");
var forward = document.querySelector("#forward");
var backward = document.querySelector("#backward");
var myPlayList = document.querySelector("#my-playlist")
var playlistName = document.querySelector('.playlist-name')
var playBtn = document.querySelector('#play-btn')
var selectedSong = 0;
var flag = 0;
var artistflag = 0;
var playBtnFlag = 0;
var audio = new Audio();
// console.log(leftSongList)



function musicList(){
    var clutter = '';
    songs.forEach(function(elem,idx){
        clutter += `<div class="song" id=${idx}>
        <img id=${idx}  src=${elem.poster}>
        <div id=${idx} class="song-info">
            <p id=${idx} class="song-name">${elem.songName}</p>
            <p id=${idx} class="artist-name">${elem.artistName}</p>
        </div>
    </div>`
    leftSongList.innerHTML = clutter;
    audio.src = songs[selectedSong].url
    
    })
}
musicList();


myPlayList.addEventListener('click',function(){
    artistflag=0;
    play.innerHTML = `<i class="ri-play-fill"></i>`
    playlistName.textContent = "My PlayList";
    musicList();
    
})

playBtn.addEventListener('click',function(){
    if (playBtnFlag == 0) {
        audio.src = songs[songs.length-1].url
        audio.play();
        playBtn.textContent = "Pause"
        play.innerHTML = `<i class="ri-pause-mini-fill"></i>`
        playBtnFlag = 1
    }else{
        audio.pause();
        playBtn.textContent = "Play"
        play.innerHTML = `<i class="ri-play-fill"></i>`
        playBtnFlag = 0;
    }
})


